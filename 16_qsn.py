# Wap to detect double space in a string
a="My name is aagaman  bhattarai."
print(a.find("  "))
# if there isnt double space in the string then it will return -1 whereas it will return the index if there is double space in string


# Wap to replace double space to single space in a string
a="My name is aagaman  bhattarai."
print(a.replace("  "," "))
# if there isnt double space in the string then it will return -1 whereas it will return the index if there is double space in string